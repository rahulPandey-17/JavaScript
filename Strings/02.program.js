// string methods

let text = "Pavilion";

// length
console.log(text.length);

// toUpperCase
console.log(text.toUpperCase());

// toLowerCase
console.log(text.toLowerCase());

// slice
console.log(text.slice(1, 5));

// replace
console.log(text.replace("P", "R"));

// trim(removes white spaces)
console.log(text.trim());